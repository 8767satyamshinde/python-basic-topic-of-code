
#1. Single Quote
txt = 'It\'s alright.'
print("1. Single Quote : ",txt)

#2. \\	Backslash
txt = "This will insert one \\ (backslash)."
print("2.  \\	Backslash : ",txt)

#3.  \n	New Line
txt = "Hello\n               World!"
print("3. New Line : ",txt)

#4.  \r	Carriage Return
txt = "Hello\rWorld!"
print(txt,"4. Carriage Return : ")
print("Hello, World!\rPython")  # This will print "Python, World!" because \r moves the cursor back to the beginning of the line before printing "Python"

#5. \t	Tab
txt = "Hello\tWorld!"
print("Tab \t\t : ",txt)


#6. This example erases one character (backspace):
txt = "Hello \bWorld!"
print("6. backslace b than backspace form like  : ",txt)





